#include <stdio.h>
/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: addition of integer
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * Return: the subtraction of the integer
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * add - divide two integers
 * @a: first int
 * @b: second int
 * Return: the divisible of the integer
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 * Return: multiplication of integer
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - modulus two integers
 * @a: first int
 * @b: second int
 * Return: the remainder of integer
 */
int mod(int a, int b)
{
	return (a % b);
}
